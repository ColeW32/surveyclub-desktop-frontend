import React, { useState } from 'react';
import { Bug } from 'lucide-react';
import { useUser } from '../context/UserContext';

const LoginScreen: React.FC = () => {
  const { setIsAuthenticated } = useUser();
  const [error, setError] = useState<string | null>(null);
  const [isSigningIn, setIsSigningIn] = useState(false);

  const handleGoogleSignIn = async () => {
    setIsSigningIn(true);
    // Mock login delay
    setTimeout(() => {
      setIsAuthenticated(true);
      setIsSigningIn(false);
    }, 1000);
  };

  const loginAsDebugUser = () => {
      setIsAuthenticated(true);
  }

  return (
    <div className="flex h-screen bg-[#0a0a0a] text-white overflow-hidden">
      {/* Background decorations */}
      <div className="absolute top-1/4 -right-32 w-96 h-96 bg-[#c9ff3a] rounded-full blur-[200px] opacity-10 pointer-events-none"></div>
      <div className="absolute bottom-1/4 -left-32 w-96 h-96 bg-blue-500 rounded-full blur-[200px] opacity-5 pointer-events-none"></div>

      <div className="flex-1 flex items-center justify-center relative">
        <div className="w-full max-w-sm px-6">
          {/* Logo / Brand */}
          <div className="text-center mb-10">
            <div className="inline-flex items-center justify-center w-16 h-16 bg-[#c9ff3a] rounded-2xl mb-6 shadow-lg shadow-[#c9ff3a]/20">
              <span className="text-black text-2xl font-black">S</span>
            </div>
            <h1 className="text-3xl font-black tracking-tight mb-2">SurveyClub</h1>
            <p className="text-sm text-gray-500 font-medium">Sign in to start earning</p>
          </div>

          {/* Google Sign-In Button */}
          <button
            onClick={handleGoogleSignIn}
            disabled={isSigningIn}
            className="w-full flex items-center justify-center space-x-3 px-6 py-3.5 bg-white text-black rounded-xl font-semibold text-sm hover:bg-gray-100 transition-all disabled:opacity-50 disabled:cursor-not-allowed shadow-lg shadow-white/5"
          >
            {isSigningIn ? (
              <div className="w-5 h-5 border-2 border-gray-400 border-t-black rounded-full animate-spin"></div>
            ) : (
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4" />
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
              </svg>
            )}
            <span>{isSigningIn ? 'Signing in...' : 'Continue with Google'}</span>
          </button>

          {/* Error Message */}
          {error && (
            <div className="mt-4 p-3 bg-red-500/10 border border-red-500/20 rounded-xl">
              <p className="text-xs text-red-400 text-center">{error}</p>
            </div>
          )}

          {/* Divider */}
          <div className="flex items-center my-6">
            <div className="flex-1 h-px bg-gray-800"></div>
            <span className="px-4 text-[10px] text-gray-600 uppercase tracking-widest font-bold">Dev</span>
            <div className="flex-1 h-px bg-gray-800"></div>
          </div>

          {/* Debug Bypass */}
          <button
            onClick={loginAsDebugUser}
            className="w-full flex items-center justify-center space-x-2 px-6 py-3 bg-gray-900/60 border border-gray-800 text-gray-400 rounded-xl text-sm font-medium hover:bg-gray-800 hover:text-white hover:border-gray-700 transition-all"
          >
            <Bug size={16} />
            <span>Debug Bypass</span>
          </button>
          <p className="text-[10px] text-gray-700 text-center mt-2">Skips auth with a dummy account</p>
        </div>
      </div>
    </div>
  );
};

export default LoginScreen;
